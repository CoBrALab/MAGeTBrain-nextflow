function extension_strip() {
  sed -r 's/(.nii$|.nii.gz|.nrrd|.mnc|.mnc.gz)$//'
}

failure_handler() {
  local lineno=$2
  local fn=$3
  local exitstatus=$4
  local msg=$5
  local lineno_fns=${1% 0}
  if [[ "$lineno_fns" != "0" ]]; then
    lineno="${lineno} ${lineno_fns}"
  fi
  echo "${BASH_SOURCE[1]}:${fn}[${lineno}] Failed with status ${exitstatus}: $msg"
}
trap 'failure_handler "${BASH_LINENO[*]}" "$LINENO" "${FUNCNAME[*]:-script}" "$?" "$BASH_COMMAND"' ERR

# Calculates N "equally" spaced values between A and B
slice_spacing() {
    local A=$1
    local B=$2
    local N=$3

    awk -v A="$A" -v B="$B" -v N="$N" '
    BEGIN {
        step = (B - A) / (N - 1)
        for (i = 0; i < N; i++) {
            point = A + i * step
            # Round to nearest integer
            rounded = (point >= 0) ? int(point + 0.5) : int(point - 0.5)
            print rounded
        }
    }'
}

